DROP DATABASE IF EXISTS EmpresaPedidos;

CREATE DATABASE EmpresaPedidos;
USE EmpresaPedidos;

CREATE TABLE Usuario (
	idUsuario INTEGER NOT NULL AUTO_INCREMENT,
	Login VARCHAR(10) NOT NULL,
	Ativo INTEGER NOT NULL,
	Senha VARCHAR(100) NOT NULL,
	NomeUsuario VARCHAR(30) NOT NULL,
	PRIMARY KEY(idUsuario),
	UNIQUE uLOGIN (Login)
);

CREATE TABLE Produto (
	idProduto INTEGER NOT NULL AUTO_INCREMENT,
	Descricao VARCHAR(100),
	NomeProduto VARCHAR(50) NOT NULL,
	ValorUnitario DECIMAL(10,2) NOT NULL,
	PRIMARY KEY(idProduto),
	UNIQUE uNomeProduto(NomeProduto)
);

CREATE TABLE TipoContato (
	idTipoContato INTEGER NOT NULL AUTO_INCREMENT,
	NomeTipoContato VARCHAR(20) NOT NULL,
	PRIMARY KEY(idTipoContato),
	UNIQUE uNomeTipoContato(NomeTipoContato)
);

CREATE TABLE Setor (
	idSetor INTEGER NOT NULL AUTO_INCREMENT,
	NomeSetor VARCHAR(30) NOT NULL,
	PRIMARY KEY(idSetor),
	UNIQUE uNomeSetor(NomeSetor)
);

CREATE TABLE Classificacao (
	idClassificacao INTEGER NOT NULL AUTO_INCREMENT,
	NomeClassificacao VARCHAR(20) NOT NULL,
	PRIMARY KEY (idClassificacao),
	UNIQUE uNomeClassificacao(NomeClassificacao)
);

CREATE TABLE Cidade (
	idCidade INTEGER NOT NULL AUTO_INCREMENT,
	NomeCidade VARCHAR(30) NOT NULL,
	SiglaEstado VARCHAR(2) NOT NULL,
	PRIMARY KEY(idCidade),
	UNIQUE uCidadeUF(NomeCidade,SiglaEstado)
);

CREATE TABLE Cliente (
	idCliente INTEGER NOT NULL AUTO_INCREMENT,
	CNPJ_CPF VARCHAR(20),
	Tipo CHAR(1), 
	Nome VARCHAR(80) NOT NULL,
	Observacao VARCHAR(200),
	DataCadastro DATETIME NOT NULL,
	DataAltercao DATETIME,
	fkClassificacao INTEGER NOT NULL,
	fkSetor INTEGER NOT NULL,
	fkUsuario INTEGER NOT NULL,
	PRIMARY KEY(idCliente),
	FOREIGN KEY(fkUsuario) REFERENCES Usuario (idUsuario),
	FOREIGN KEY(fkClassificacao) REFERENCES Classificacao (idClassificacao),
	FOREIGN KEY(fkSetor) REFERENCES Setor (idSetor)
);

CREATE TABLE Telefones (
	idTelefone INTEGER NOT NULL AUTO_INCREMENT,
	Descricao VARCHAR(10) NOT NULL,
	DDD INTEGER NOT NULL,
	NumeroFone INTEGER NOT NULL,
	fkCliente INTEGER NOT NULL,
	PRIMARY KEY(idTelefone),
	FOREIGN KEY(fkCliente) REFERENCES Cliente (idCliente) ON DELETE CASCADE
);

CREATE TABLE Enderecos (
	idEndereco INTEGER NOT NULL AUTO_INCREMENT,
	CEP VARCHAR(10) NOT NULL,
	Endereco VARCHAR(60) NOT NULL,
	Numero VARCHAR(10),
	Complemento VARCHAR(20),
	Bairro VARCHAR(50),
	fkCliente INTEGER NOT NULL,
	fkCidade INTEGER NOT NULL,
	PRIMARY KEY(idEndereco),
	FOREIGN KEY(fkCliente) REFERENCES Cliente (idCliente) ON DELETE CASCADE,
	FOREIGN KEY(fkCidade) REFERENCES Cidade (idCidade)
);

CREATE TABLE Contato (
	idContato INTEGER NOT NULL AUTO_INCREMENT,
	NomeContato VARCHAR(80) NOT NULL,
	Email VARCHAR(10),
	Aniversario VARCHAR(5),
	Observacao VARCHAR(10),
	NumeroCelular VARCHAR(10),
	DDD VARCHAR(10),
	fkUsuario INTEGER NOT NULL,
	fkTipoContato INTEGER NOT NULL,
	fkCliente INTEGER NOT NULL,
	PRIMARY KEY(idContato),
	UNIQUE uContatoCliente(NomeContato,fkCliente),
	FOREIGN KEY(fkUsuario) REFERENCES Usuario (idUsuario),
	FOREIGN KEY(fkTipoContato) REFERENCES TipoContato (idTipoContato),
	FOREIGN KEY(fkCliente) REFERENCES Cliente (idCliente) ON DELETE CASCADE
);

CREATE TABLE Pedido (
	idPedido INTEGER NOT NULL AUTO_INCREMENT,
	ValorTotal DECIMAL(10,2) NOT NULL,
	Acrescimo DECIMAL(2,2),
	Observacao VARCHAR(200),
	Desconto NUMERIC(2,2),
	PrevisaoEntrega DATETIME,
	DataPedido DATETIME NOT NULL,
	fkUsuario INTEGER NOT NULL,
	fkCliente INTEGER NOT NULL,
	fkUsuarioVendedor INTEGER NOT NULL,
	PRIMARY KEY(idPedido),
	FOREIGN KEY(fkUsuario) REFERENCES Usuario (idUsuario),
	FOREIGN KEY(fkUsuarioVendedor) REFERENCES Usuario (idUsuario),
	FOREIGN KEY(fkCliente) REFERENCES Cliente (idCliente)
);

CREATE TABLE PedidoItens (
	idItem INTEGER NOT NULL AUTO_INCREMENT,
	Quantidade INTEGER NOT NULL,
	fkPedido INTEGER NOT NULL,
	fkProduto INTEGER NOT NULL,	
	PRIMARY KEY(idItem),
	FOREIGN KEY(fkPedido) REFERENCES Pedido (idPedido),
	FOREIGN KEY(fkProduto) REFERENCES Produto (idProduto)
);


